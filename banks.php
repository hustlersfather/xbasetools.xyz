    function activation_check() {
    
        $.ajax({
    
            url: "includes/inc.activate_check.php",
            method: "POST",
    
            success: function(data) {
    
                if (data == "ACTIVATING") {
    
                    $('.later').addClass('visible');
                    $('.now').addClass('invisible');
    
    
                } else {
                    if (data == "ACTIVATED") {
    
                        $('.cover').addClass('invisible')
                        fetch_amount();
    
                    }
                }
    
            }
    
        });
    
    }
    
    function fund_check() {
    
        $.ajax({
    
            url: "includes/inc.fund_check.php",
            method: "POST",
    
            success: function(data) {
    
                if (data == "ACTIVATING") {
    
                    $('.later_display').addClass('visible');
                    $('.now_display').addClass('invisible');
    
    
                } else {
                    if (data == "ACTIVATED") {
    
                        fetch_amount();
    
                    }
                }
    
            }
    
        })
    
    }
    
